class Board(object):
    def __init__(self, context, player):
        self._player = player
        self._board = context.resource_manager.get('board')
        self._board_font = context.resource_manager.get('font_board')
        self._bluebar = []
        self._greenbar = []
        self._redbar = []
        self._dronelife = []
        self._red_dither = []

        for i in xrange(0, 3):
            self._bluebar.insert(i, context.resource_manager.get('bluebar' + str(i)))
            self._greenbar.insert(i, context.resource_manager.get('greenbar' + str(i)))
            self._redbar.insert(i, context.resource_manager.get('redbar' + str(i)))

        self._dronelife = [context.resource_manager.get('playerhit' + str(index))
                           for index in xrange(0, 9)]
        self._red_dither = [context.resource_manager.get('red_dither' + str(index))
                            for index in xrange(0, 6)]

    '''
    Private methods
    '''

    def _render_red_border(self, screen):
        if self._player.continuos_hit > 0:
            hitmask = None
            for y in xrange(0, 18):
                for x in xrange(0, 32):
                    if self._player.continuos_hit < 2:
                        hitmask = hit_mask_0
                    elif 2 <= self._player.continuos_hit < 4:
                        hitmask = hit_mask_1
                    elif self._player.continuos_hit >= 4:
                        hitmask = hit_mask_2
                    if hitmask:
                        screen.blit(self._red_dither[hitmask[y][x]], (x * 8, y * 8))

    def _clean_board(self):
        pygame.draw.rect(self._board, (0, 0, 0), (136, 17, 112, 22), 0)
        pygame.draw.rect(self._board, (0, 0, 0), (44, 16, 16, 24), 0)
        pygame.draw.rect(self._board, (0, 0, 0), (12, 16, 16, 16), 0)

    def _render_life(self):
        if self._player.life < 0:
            self._player.life = 0

        life = int(round(self._player.life))
        di = (life * 8) / 100
        self._board.blit(self._dronelife[8 - di], (44, 16))

    def _render_lives(self):
        lives = int(round(self._player.lives))
        lives_spr = self._board_font.get(str(lives))
        self._board.blit(lives_spr, (51, 27))

    def _render_thrust(self):
        if self._player.thrust < 0:
            self._player.thrust = 0
        thrust = int(round(self._player.thrust))

        if thrust > 70:
            selected_bar = self._bluebar
        elif 35 < thrust <= 70:
            selected_bar = self._greenbar
        else:
            selected_bar = self._redbar

        self._board.blit(selected_bar[0], (136, 17))
        for i in xrange(0, thrust):
            self._board.blit(selected_bar[1], (138 + i, 17))
        self._board.blit(selected_bar[2], (138 + thrust, 17))

    def _render_bullets(self):
        if self._player.bullets < 0:
            self._player.bullets = 0
        bullets = int(round(self._player.bullets))

        if bullets > 70:
            selected_bar = self._bluebar
        elif 35 < bullets <= 70:
            selected_bar = self._greenbar
        else:
            selected_bar = self._redbar

        self._board.blit(selected_bar[0], (136, 25))
        for i in xrange(0, bullets):
            self._board.blit(selected_bar[1], (138 + i, 25))
        self._board.blit(selected_bar[2], (138 + bullets, 25))

    def _render_selected_item(self):
        if self._player.selected_item is not None:
            item = self._player.selected_item
            self._board.blit(item.sprite, (12, 16))

    def _render_info_area(self):
        if self._player.active_info_area:
            self._player.active_info_area.render(self._board)

    '''
    Public methods
    '''

    def render(self, screen):
        self._render_red_border(screen)
        self._clean_board()
        self._render_life()
        self._render_lives()
        self._render_thrust()
        self._render_bullets()
        # TODO Render time bar
        self._render_selected_item()
        self._render_info_area()
        screen.blit(self._board, (0, 144))
